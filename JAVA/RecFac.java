import java.util.Scanner;
public class RecFac {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter n:");
        int n = sc.nextInt();
        System.out.println("The factorial of "+n+" is:"+Fact(n));
    }
    public static int Fact(int n){
        if (n == 0 || n==1){
            return 1;
        }
    return n * Fact(n-1);
    }
}
